        document.onkeydown=function(){
            if (window.event.keyCode >= 65 && window.event.keyCode <= 90) {
                openSearchModal()
                document.getElementById("search_query").focus();
            }
            if(window.event.keyCode === 27 ){
                    closeSearchModal()
